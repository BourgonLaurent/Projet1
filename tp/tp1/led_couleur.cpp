#include <avr/io.h> 
#include <util/delay_basic.h>

int main()
{
  DDRA = 0xff; // PORT A est en mode sortie
  DDRB = 0xff; // PORT B est en mode sortie
  DDRC = 0xff; // PORT C est en mode sortie
  DDRD = 0xff; // PORT D est en mode sortie
    
  for(;;)  // boucle sans fin
  {
    _delay_ms(99);
    
  }
  return 0; 
}

